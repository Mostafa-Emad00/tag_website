// Function to set language for the page
function setLanguage(language) {
    localStorage.setItem('selectedLanguage', language); // Store language in localStorage
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(el => {
        const key = el.getAttribute('data-translate');
        el.textContent = translations[language][key] || el.textContent;
    });
}

// Function to initialize language on page load
function initializeLanguage() {
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'en'; // Default to 'en' if no language is stored
    setLanguage(savedLanguage);
}

// Event listeners for language dropdown
document.querySelectorAll('.dropdown-item').forEach(item => {
    item.addEventListener('click', function(event) {
        event.preventDefault();
        const selectedLanguage = this.getAttribute('data-lang');
        setLanguage(selectedLanguage);
        
        // Optionally, close the dropdown after selection
        const dropdown = new bootstrap.Dropdown(document.getElementById('languageDropdown'));
        dropdown.hide();
    });
});

// Initialize language when the page is loaded
document.addEventListener('DOMContentLoaded', () => {
    initializeLanguage();
});


